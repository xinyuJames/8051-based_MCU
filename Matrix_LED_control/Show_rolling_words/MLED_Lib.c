void MLEDShow_HELLO()
{
	
	unsigned int i;
	unsigned int j;
	unsigned int rep;
	for (i = 0; i < 44 - 8; i++)
	{
		
		for (rep = 0; rep < 8; rep++)
		{
			for (j = 0; j < 8; j++)
			{
				MLED_ShowColumn(j, HELLO[j + i]);
			}
		}
	}
}