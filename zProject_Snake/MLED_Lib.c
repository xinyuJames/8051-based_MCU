void MLEDShow_GAMEOVER()
{
	unsigned int i;
	unsigned int j;
	unsigned int rep;
	for (i = 0; i < 34 - 8; i++)
	{
		for (rep = 0; rep < 8; rep++)
		{
			for (j = 0; j < 8; j++)
			{
				MLED_ShowColumn(j, A_GAMEOVER[j + i]);
			}
		}
	}
}

void MLEDShow_SCORE(unsigned char score)
{
	unsigned int i;
	unsigned int j;
	unsigned int rep;
	
	for (i = 0; i < 34; i++)
	{
		for (rep = 0; rep < 8; rep++)
		{
			for (j = 0; j < 8; j++)
			{
				MLED_ShowColumn(j, A_SCORE[j + i]);
			}
		}
	}
	
	for (rep = 0; rep < 300; rep++)
	{
		for (j = 0; j < 4; j++)
		{
			MLED_ShowColumn(j, A_numberList[score / 10][j]);
		}
		
		for (j = 0; j < 4; j++)
		{
			MLED_ShowColumn(j + 4, A_numberList[score % 10][j]);
		}
	}
}

